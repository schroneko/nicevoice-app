import Foundation

enum ObfuscatedStrings {
    static var keychainService: String {
        deobfuscate(
            data: [0xDF, 0x58, 0xE0, 0x74, 0x48, 0x38, 0x74, 0x6C, 0xC3, 0x81, 0x5F, 0x16, 0xFA, 0x7F, 0xC9, 0x04, 0x51, 0xE0, 0xC8, 0x87, 0x3D, 0xF5, 0x55],
            key: [0xBE, 0x28, 0x90, 0x5A, 0x26, 0x51, 0x17, 0x09, 0xB5, 0xEE, 0x36, 0x75, 0x9F, 0x51, 0x87, 0x6D, 0x32, 0x85, 0x9E, 0xE8, 0x54, 0x96, 0x30]
        )
    }

    private static func deobfuscate(data: [UInt8], key: [UInt8]) -> String {
        String(bytes: zip(data, key).map { $0 ^ $1 }, encoding: .utf8) ?? ""
    }
}
